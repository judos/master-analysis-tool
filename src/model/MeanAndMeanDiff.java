package model;

/**
 * @since 20.05.2014
 * @author Julian Schelker
 */
public class MeanAndMeanDiff {

	private double mean;
	private double avgDiff;

	public MeanAndMeanDiff(double mean, double maxDiff) {
		this.mean = mean;
		this.avgDiff = maxDiff;
	}

	/**
	 * @return the mean
	 */
	public double getMean() {
		return mean;
	}

	/**
	 * @return the maxDiff
	 */
	public double getAvgDiff() {
		return avgDiff;
	}

	@Override
	public String toString() {
		return "Mean: " + this.mean + ", AvgDiff: " + avgDiff;
	}

}
