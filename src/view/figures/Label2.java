
package view.figures;

import java.awt.Graphics;


/**
 * @since 23.05.2014
 * @author Julian Schelker
 */
public class Label2 {

	private Label l1;
	private Label l2;

	public Label2(String t1, String t2) {
		l1 = new Label(t1);
		l2 = new Label(t2);
	}

	public void drawAround(Graphics g, int x, int y) {
		l1.drawLeftFrom(g, x, y);
		l2.drawRightFrom(g, x, y);
	}
}
